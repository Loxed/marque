function compileMqs(source, options = {}) {
  const sourceFile = options.sourceFile || '<inline>';
  const rootDir = options.rootDir || path.dirname(sourceFile);
  const seen = options.seen || new Set();

  const realSource = safeRealpath(sourceFile);
  if (realSource && seen.has(realSource)) {
    throw new Error(`MQS circular import detected: ${sourceFile}`);
  }
  if (realSource) seen.add(realSource);

  const expandedImports = expandImports(String(source || '').replace(/^\uFEFF/, ''), {
    sourceFile,
    rootDir,
    seen,
  });

  const expandedDirectives = expandDirectives(expandedImports, sourceFile);

  if (realSource) seen.delete(realSource);
  return expandedDirectives;
}

function expandImports(source, options) {
  const { sourceFile, rootDir, seen } = options;
  const lines = source.split(/\r?\n/);
  const out = [];

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const m = line.match(/^\s*@mqs-import\s+['\"]([^'\"]+)['\"]\s*;\s*$/);
    if (!m) {
      out.push(line);
      continue;
    }

    const rel = m[1].trim();
    const target = path.resolve(rootDir, rel);
    if (!fs.existsSync(target)) {
      throw new Error(`MQS import not found in ${sourceFile}:${i + 1} -> ${rel}`);
    }

    const importedSrc = fs.readFileSync(target, 'utf8');
      const importedCss = compileMqs(importedSrc, {
      sourceFile: target,
      rootDir: path.dirname(target),
      seen,
    });

    out.push(`/* mqs-import: ${rel} */`);
    out.push(importedCss);
    out.push(`/* end mqs-import: ${rel} */`);
  }

  return out.join('\n');
}

function expandDirectives(source, sourceFile) {
  let palette = { ...DEFAULT_PALETTE };
  let css = String(source || '');

  css = css.replace(/@mqs-palette\s*\{([\s\S]*?)\}\s*;?/gi, (_, block) => {
    palette = { ...palette, ...parsePaletteBlock(block, sourceFile) };
    return '';
  });

  css = css.replace(/@mqs-essentials\s*;/gi, () => buildEssentialsCSS(palette));
  return css;
}

function parsePaletteBlock(block, sourceFile) {
  const values = {};
  const cleaned = String(block || '')
    .replace(/\/\*[\s\S]*?\*\//g, '')
    .replace(/(^|\n)\s*\/\/.*(?=\n|$)/g, '$1');

  const pairRe = /([a-z][a-z0-9-]*)\s*:\s*([^;]+);?/gi;
  let match;
  while ((match = pairRe.exec(cleaned))) {
    const key = match[1].toLowerCase();
    const value = match[2].trim();
    if (!value) continue;
    if (key === 'bg') {
      values.background = value;
      continue;
    }
    if (key === 'accent') {
      values.primary = value;
      continue;
    }
    if (key === 'accent2') {
      values.secondary = value;
      continue;
    }
    values[key] = value;
  }

  const allowed = new Set(Object.keys(DEFAULT_PALETTE));
  for (const key of Object.keys(values)) {
    if (!allowed.has(key)) {
      throw new Error(`Unknown MQS palette key in ${sourceFile}: ${key}`);
    }
  }

  return values;
}


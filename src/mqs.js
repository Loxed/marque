function safeRealpath(filePath) {
  try {
    return fs.realpathSync(filePath);
  } catch (_) {
    return null;
  }
}

function compileMqsFile(filePath) {
  const source = fs.readFileSync(filePath, 'utf8');
  return compileMqs(source, {
    sourceFile: filePath,
    rootDir: path.dirname(filePath),
    seen: new Set(),
  });
}

